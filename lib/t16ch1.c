#include "s1c17702.h"
#include "t16.h"

/****************************************************************************
*	prototype
*****************************************************************************/
void t16Init1(T_T16_PARAM *param);


/****************************************************************************
*	Initialize process.
*	param:	param	in	initialize parameter
*	return:	none
*****************************************************************************/
void t16Init1(T_T16_PARAM *param)
{
	t16Stop1();
	t16IntDisable0();
	
	*T16_CLK1 = (param->pclkDiv & T16_CLK1_DF);
	*T16_TR1 = (param->reload);
	if(param->mode){
		*T16_CTL1 |= T16_CTL1_TRMD;
	}
	else{
		*T16_CTL1 &= ~T16_CTL1_TRMD;
	}
	
	return;
}
