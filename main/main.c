#include "s1c17702.h"
#include "system.h"
#include "port.h"

/****************************************************************************
*	prottype
*****************************************************************************/

/****************************************************************************
*	main function.
*	param:	none
*	return:	error code
*****************************************************************************/
int main(void)
{
	Reg_P2xMux.x7		= PORT_FUNC_0;	// P27 is port
	Reg_P2xOutEna.x7	= PORT_ENABLE;	// P27 output setting
	Reg_P2xOut.x7		= PORT_HIGH;	// initial High

	while(1){
		Wait(1000);//1[s]
		Reg_P2xOut.x7		= PORT_LOW;
		Wait(1000);//1[s]
		Reg_P2xOut.x7		= PORT_HIGH;
	}

	return 0;
}
